/**************************************************************************//**
* @fn			void LCD_drawCircle(uint8_t x0, uint8_t y0, uint8_t radius,uint16_t color)
* @brief		Draw a colored circle of set radius at coordinates
* @note
*****************************************************************************/
void LCD_drawCircle(uint8_t x0, uint8_t y0, uint8_t radius, uint16_t color)
{
	for (int i = 0; i <= 4; i++) {
		int start_x = x0 - (i * radius / 5);
		int end_x = x0 + (i * radius / 5);
		int height = sqrt(radius*radius - (i * radius / 5)*(i * radius / 5));
		LCD_setAddr(start_x, y0 - height, end_x, y0 + height);
		for (int x = start_x; x <= end_x; x++) {
			for (int y = y0 - height; y <= y0 + height; y++) {
				SPI_ControllerTx_16bit(color);
		}
	}
	}
}



/**************************************************************************//**
* @fn			void LCD_drawLine(short x0,short y0,short x1,short y1,uint16_t c)
* @brief		Draw a line from and to a point with a color
* @note
*****************************************************************************/
void LCD_drawLine(short x0,short y0,short x1,short y1,uint16_t c)
{
  int dx = abs(x1 - x0);
  int dy = abs(y1 - y0);
  int sx = x0 < x1 ? 1 : -1;
  int sy = y0 < y1 ? 1 : -1;
  int err = dx - dy;

  while (x0 != x1 || y0 != y1) {
	  LCD_drawPixel(x0, y0, c);
	  int e2 = err * 2;
	  if (e2 > -dy) {
		  err -= dy;
		  x0 += sx;
	  }
	  if (e2 < dx) {
		  err += dx;
		  y0 += sy;
	  }
  }

  LCD_drawPixel(x1, y1, c);
}



/**************************************************************************//**
* @fn			void LCD_drawBlock(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1,uint16_t color)
* @brief		Draw a colored block at coordinates
* @note
*****************************************************************************/
void LCD_drawBlock(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1,uint16_t color)
{
{
	LCD_setAddr(x0,y0,x1,y1);
	for (int i = x0; i<= x1; i++) {
	for (int j = y0; j<= y1; j++) {
	SPI_ControllerTx_16bit(color);
	}
	}
}
}

/**************************************************************************//**
* @fn			void LCD_setScreen(uint16_t color)
* @brief		Draw the entire screen to a color
* @note
*****************************************************************************/
void LCD_setScreen(uint16_t color) 
{
  int screenWidth = 159;
  int screenHeight = 127;
  LCD_drawBlock(0, 0, screenWidth, screenHeight, color);
}

/**************************************************************************//**
* @fn			void LCD_drawString(uint8_t x, uint8_t y, char* str, uint16_t fg, uint16_t bg)
* @brief		Draw a string starting at the point with foreground and background colors
* @note
*****************************************************************************/
void LCD_drawString(uint8_t x, uint8_t y, char* str, uint16_t fg, uint16_t bg)
{
    uint8_t charWidth = 6; // each character is 5 pixels wide, plus 1 pixel for spacing
    uint8_t charHeight = 8;
    uint8_t cx = x;

    for (; *str; str++) {
	    char c = *str;
	    LCD_drawChar(cx, y, c, fg, bg);
	    cx += charWidth;
    }
}
